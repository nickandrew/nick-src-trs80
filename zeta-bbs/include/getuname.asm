_GETUNAME:
	LD	HL,2
	ADD	HL,SP
	LD	E,(HL)
	INC	HL
	LD	D,(HL)
;
	IFDEF	ZETA
	LD	HL,(USR_NAME)
	ELSE
	LD	HL,DUMMY_NAME
	ENDIF
;
GETU_01	LD	A,(HL)
	LD	(DE),A
	OR	A
	RET	Z
	CP	CR
	JR	Z,GETU_02
	INC	HL
	INC	DE
	JR	GETU_01
GETU_02
	XOR	A
	LD	(DE),A
	RET
;
	IFNDEF	ZETA
DUMMY_NAME	DEFM	'Dummy',0
	ENDIF
;
