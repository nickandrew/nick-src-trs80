;
;SPRINT_NUMB. Print a number in HL to a string at DE.
;
SPRINT_NUMB
	LD	($SPRNU_DEV),DE
	XOR	A
	LD	($SBLANK),A
	LD	DE,10000
	CALL	$SPRT_DIGIT
	LD	DE,1000
	CALL	$SPRT_DIGIT
	LD	DE,100
	CALL	$SPRT_DIGIT
	LD	DE,10
	CALL	$SPRT_DIGIT
	LD	DE,1
	LD	A,E
	LD	($SBLANK),A
	CALL	$SPRT_DIGIT
	XOR	A
	LD	(DE),A
	RET
;
$SPRT_DIGIT
	LD	B,'0'-1
$SPD_1	INC	B
	OR	A
	SBC	HL,DE
	JR	NC,$SPD_1
	ADD	HL,DE
	LD	A,($SBLANK)
	OR	A
	JR	NZ,$SPD_2
	LD	A,B
	LD	($SDIGIT),A
	CP	'0'
	RET	Z
$SPD_2	LD	($SBLANK),A
	LD	A,B
	LD	($SDIGIT),A
	LD	DE,($SPRNU_DEV)
	LD	(DE),A
	INC	DE
	LD	($SPRNU_DEV),DE
	RET
;
$SBLANK		DEFB	0
$SDIGIT		DEFB	0
$SPRNU_DEV	DEFW	0
;
