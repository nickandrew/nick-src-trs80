;Id: Print user's name and user-number.
;
*GET	DOSCALLS
*GET	EXTERNAL
*GET	ASCII
;
	ORG	PROG_START
	DEFW	BASE
	DEFW	THIS_PROG_END
	DEFW	TERMINATE
	DEFW	TERMINATE
;End of program load info.
;
	COM	'<ID 1.0a 13-Apr-86>'
	ORG	BASE+100H
START
	LD	SP,START
;
	LD	HL,M_UNAME
	LD	DE,($STDOUT)
	CALL	MESS_0
;
	LD	HL,(USR_NAME)
	LD	DE,($STDOUT)
	CALL	MESS_NCR
;
	LD	HL,M_UNUM
	LD	DE,($STDOUT)
	CALL	MESS_0
;
	LD	HL,(USR_NUMBER)
	LD	DE,($STDOUT)
	CALL	PRINT_NUMB_DEV
;
	LD	A,CR
	CALL	STD_OUT
;
	LD	A,0
	JP	TERMINATE
;
MESS_NCR
	LD	A,(HL)
	OR	A
	RET	Z
	CP	ETX
	RET	Z
	CP	CR
	RET	Z
	CALL	$PUT
	INC	HL
	JR	MESS_NCR
;
*GET	ROUTINES
;
M_UNUM
	DEFM	'   User number: ',0
M_UNAME
	DEFM	'User name: ',0
;
THIS_PROG_END	EQU	$
;
	END	START
