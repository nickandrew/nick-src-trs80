	CALL	WRITE2
	LD	A,2
	CALL	WRITE2
	LD	A,L
	CALL	WRITE2
	LD	A,H
	CALL	WRITE2
	LD	B,0
WRBV01	LD	A,(DE)
	CALL	WRITE2
	INC	HL
	INC	DE
	DJNZ	WRBV01
	DEC	C
	JR	NZ,WRBLOC
	LD	A,1
	CALL	WRITE2
	LD	A,17
	CALL	WRITE2
	LD	A,L
	CALL	WRITE2
	LD	A,H
	CALL	WRITE2
	LD	B,15
	LD	HL,DUMMY
