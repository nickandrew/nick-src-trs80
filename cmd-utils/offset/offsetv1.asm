	CALL	WRITE2
	INC	HL
	DJNZ	WRBLOC2
	LD	A,2
	CALL	WRITE2
	LD	A,2
	CALL	WRITE2
	LD	HL,(DLDA)
	LD	DE,1200H
	ADD	HL,DE
	LD	A,L
	CALL	WRITE2
	LD	A,H
	CALL	WRITE2
	LD	DE,FCB1
	CALL	DOS_CLOSE
	LD	DE,FCB2
	CALL	DOS_CLOSE
	JP	DOS
