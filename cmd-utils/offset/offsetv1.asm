	DEFB	03H
LENOK	DEC	HL
	DEC	HL
	DEC	HL
	DEC	HL
	LD	E,(HL)
	INC	HL
	LD	D,(HL)
	EX	DE,HL
	LD	(DLDA),HL
	LD	DE,8000H
	LD	C,18
