	LD	DE,FCB1
	CALL	DOS_EXTRACT	; Extract filespec
	POP	HL
	CALL	NEXTWORD
	LD	DE,FCB2
	CALL	DOS_EXTRACT
	XOR	A
	LD	(COUNT_T),A
	LD	(N),A
	LD	(NUM),A
	LD	HL,BLKTBL2
	LD	(BLK2),HL
	LD	IX,BLKTBL+4
	LD	HL,DEFEXT
	LD	DE,FCB1
	CALL	DOS_EXTEND
	LD	HL,DEFEXT
	LD	DE,FCB2
	CALL	DOS_EXTEND
	LD	HL,BUFFER1
	LD	DE,FCB1
	LD	B,0
	CALL	DOS_OPEN_EX
	JP	NZ,DOS_ERROR
SEARCH	CALL	READ1
	CP	2
	JR	Z,EXECADD
	CP	5
	JR	Z,SKIP
	CP	31
	JR	Z,SKIP
;ASSUME LOAD BLOCK.
	CALL	READ1
	LD	B,A
	CALL	READ1
	LD	L,A
	CALL	READ1
	LD	H,A
	LD	(IX+0),L
	LD	(IX+1),H
	INC	IX
	INC	IX
	DEC	B
	DEC	B
